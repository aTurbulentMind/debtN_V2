<h1>Support</h1>

<div class="text_Block">
	<p class="text_Box cut_Box">
		Debt$Net® technical support plans suit different purposes. Initial Purchase Support provides 90
		days of technical phone support and comes as part of the purchase price of Debt$Net®. Other
		support plans, such as Hour Based Plans and the Unlimited Support plan provide technical support
		services for agencies after the Initial Purchase Support time expires. The paragraphs below
		explain each support plan.
	</p>
	<p class="text_Box cut_Box">
		Initial Purchase Support: The initial purchase of Debt$Net® includes telephone technical
		support. It provides five hours of time for the multi-user version, and three hours for the
		single-user version. This 90 day phase of technical support provides assistance to new customers
		for the installation and configuration of Debt$Net® software. Technicians log all time spent on
		the telephone with the Technical Support Department against the initial support hours,
		regardless of the nature of the problem. When 90 days has elapsed, or when the support time has
		been consumed, TCM offers continued support through one of the Debt$Net® support plans
		described below.
	</p>
	<p class="text_Box cut_Box">
		Hour Based Plans: The Hour Based Plans provide agencies with hour blocks of telephone technical
		support per quarter. The hour based blocks are available at a 2 hour level, 4 hour level and 8
		hour level. This plan type benefits experienced agencies with consistent support requirements.
		The Technical Support Department tracks support time and deducts it from the agency's quarterly
		time allocation. Technicians provide support during normal TCM business hours. Time deducts from
		the quarterly allocation regardless of the nature of the problem. When needed, agencies purchase
		additional support time at the published hourly rate for their level. Agencies must pay for
		additional time in advance and all time expires at the end of the quarter.
	</p>
	<p class="text_Box cut_Box">
		Unlimited Support Plan: The Unlimited Support Plan provides "as required" technical support
		during normal business hours. Technicians do not track support time and time allocations do not
		apply. The Technical Support Department provides services directly covered by the support plan
		without requiring additional time purchases. Under this plan, the agency pays a flat fee once
		per quarter. The plan covers only assistance with Debt$Net®. This plan benefits large agencies
		or agencies with high support requirements that do not want to face unplanned additional support
		expenses.
	</p>
</div>

